const MANAGED_HEADER: &str = "# This file is generated by dual.\n# Edit dual.toml instead.\n\n";
const BACKEND_LOCK_NAME: &str = "pixi.lock";
const PAK_LOCK_NAME: &str = "pak.lock";
const ENGINE_VERSION: &str = "0.70.2";

#[derive(Clone, Debug)]
pub struct EnvironmentBackend {
    root: PathBuf,
    verbose: bool,
}

impl EnvironmentBackend {
    pub fn new(root: &Path, verbose: bool) -> Self {
        Self {
            root: root.to_owned(),
            verbose,
        }
    }

    fn state_dir(&self) -> PathBuf {
        self.root.join(".dual")
    }

    fn workspace_dir(&self) -> PathBuf {
        self.state_dir().join("workspace")
    }

    fn manifest_path(&self) -> PathBuf {
        self.workspace_dir().join("pyproject.toml")
    }

    fn backend_lock_path(&self) -> PathBuf {
        self.workspace_dir().join(BACKEND_LOCK_NAME)
    }

    fn backend_metadata_path(&self) -> PathBuf {
        self.workspace_dir().join(".pixi")
    }

    fn public_lock_path(&self) -> PathBuf {
        self.root.join("dual.lock")
    }

    fn staged_pak_lock_path(&self) -> PathBuf {
        self.workspace_dir().join(PAK_LOCK_NAME)
    }

    fn ready_path(&self) -> PathBuf {
        self.state_dir().join("ready")
    }

    fn bridge_dir(&self) -> PathBuf {
        self.state_dir().join("bridge")
    }

    fn config_path(&self) -> PathBuf {
        self.state_dir().join("engine.toml")
    }

    fn r_profile_path(&self) -> PathBuf {
        self.state_dir().join("Rprofile")
    }

    fn shell_state_dir(&self) -> PathBuf {
        self.state_dir().join("shell")
    }

    fn zsh_state_dir(&self) -> PathBuf {
        self.shell_state_dir().join("zsh")
    }

    fn zshrc_path(&self) -> PathBuf {
        self.zsh_state_dir().join(".zshrc")
    }

    fn engine_home(&self) -> PathBuf {
        security::default_dual_home().join("engine")
    }

    fn managed_engine_path(&self) -> PathBuf {
        self.engine_home().join("bin").join(engine_binary_name())
    }

    fn engine_path(&self) -> Option<PathBuf> {
        env::var_os("DUAL_ENGINE_PATH")
            .map(PathBuf::from)
            .filter(|path| path.is_file())
            .or_else(|| {
                let path = self.managed_engine_path();
                path.is_file().then_some(path)
            })
    }

    fn engine_command(&self) -> Result<Command> {
        let path = self.engine_path().ok_or_else(|| {
            DualError::BackendStart("environment support has not been installed yet".to_owned())
        })?;
        self.verify_engine_path(&path)?;
        let mut command = Command::new(path);
        command.current_dir(&self.root);
        Ok(command)
    }

    fn verify_engine_path(&self, path: &Path) -> Result<()> {
        if path == self.managed_engine_path() {
            let expected = match env::var("DUAL_ENGINE_SHA256") {
                Ok(expected) => expected,
                Err(_) => engine_asset()?.sha256.to_owned(),
            }
            .to_ascii_lowercase();
            let actual = sha256_file(path)?;
            if actual != expected {
                anyhow::bail!(
                    "Dual environment support failed its integrity check. \
                     Run `dual engine update` to replace it."
                );
            }
        }
        Ok(())
    }

    fn ensure_state_paths_safe(&self) -> Result<()> {
        for path in [
            self.state_dir(),
            self.workspace_dir(),
            self.manifest_path(),
            self.backend_lock_path(),
            self.backend_metadata_path(),
            self.staged_pak_lock_path(),
            self.ready_path(),
            self.bridge_dir(),
            self.config_path(),
            self.r_profile_path(),
            self.shell_state_dir(),
            self.zsh_state_dir(),
            self.zshrc_path(),
        ] {
            security::ensure_managed_path(&self.root, &path)?;
        }
        security::reject_symlink_if_present(&self.public_lock_path(), "dual.lock")?;
        Ok(())
    }

    fn configured_command(&self, args: &[&str]) -> Result<Command> {
        let mut command = self.engine_command()?;
        if let Some((subcommand, rest)) = args.split_first() {
            command
                .arg(subcommand)
                .arg("--config-file")
                .arg(self.config_path())
                .args(rest);
        }
        Ok(command)
    }

    fn internal_command(&self, args: &[&str]) -> Result<Command> {
        let mut command = self.configured_command(args)?;
        if !env::var("DUAL_ALLOW_CREDENTIALS")
            .is_ok_and(|value| matches!(value.as_str(), "1" | "true" | "yes"))
        {
            for name in [
                "AWS_ACCESS_KEY_ID",
                "AWS_SECRET_ACCESS_KEY",
                "AWS_SESSION_TOKEN",
                "AZURE_CLIENT_SECRET",
                "CONDA_TOKEN",
                "GITHUB_TOKEN",
                "GH_TOKEN",
                "GOOGLE_APPLICATION_CREDENTIALS",
                "NPM_TOKEN",
                "PREFIX_API_KEY",
                "PYPI_TOKEN",
                "SSH_AUTH_SOCK",
                "TWINE_PASSWORD",
            ] {
                command.env_remove(name);
            }
        }
        Ok(command)
    }

    fn install_engine(&self) -> Result<()> {
        println!("Installing environment support...");
        self.prepare_engine_home()?;

        self.download_engine()?;

        if !self.managed_engine_path().is_file() {
            anyhow::bail!(
                "Environment support could not be installed. Check your network connection \
                 and try `dual up` again."
            );
        }
        println!("✓ Environment support installed");
        Ok(())
    }

    fn download_engine(&self) -> Result<()> {
        self.prepare_engine_home()?;
        let asset = engine_asset()?;
        let url = env::var("DUAL_ENGINE_DOWNLOAD_URL").unwrap_or_else(|_| asset.url.to_owned());
        let expected = env::var("DUAL_ENGINE_SHA256")
            .unwrap_or_else(|_| asset.sha256.to_owned())
            .to_ascii_lowercase();
        let temporary = self
            .engine_home()
            .join("bin")
            .join(format!("dual-engine.download-{}", unique_suffix()));
        download_file(&url, &temporary)
            .context("could not download environment support; check your network connection")?;
        let actual = sha256_file(&temporary)?;
        if actual != expected {
            let _ = fs::remove_file(&temporary);
            anyhow::bail!(
                "Environment support failed its integrity check. Expected {expected}, got {actual}."
            );
        }

        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            let mut permissions = fs::metadata(&temporary)?.permissions();
            permissions.set_mode(0o755);
            fs::set_permissions(&temporary, permissions)?;
        }

        let destination = self.managed_engine_path();
        if destination.is_file() {
            fs::remove_file(&destination)?;
        }
        fs::rename(&temporary, &destination)
            .context("could not activate the verified environment support executable")
    }

    fn prepare_engine_home(&self) -> Result<()> {
        let home = security::default_dual_home();
        security::create_private_directory(&home, "dual data directory")?;
        let engine = home.join("engine");
        security::create_private_directory(&engine, "Dual environment support directory")?;
        security::create_private_directory(
            &engine.join("bin"),
            "Dual environment support bin directory",
        )
    }

    fn prepare_state(&self) -> Result<()> {
        self.ensure_state_paths_safe()?;
        fs::create_dir_all(self.workspace_dir())
            .context("could not create the internal environment workspace")?;
        self.ensure_state_paths_safe()?;
        let environments = self.state_dir().join("environments");
        let config = format!(
            "detached-environments = {:?}\n",
            environments.to_string_lossy()
        );
        security::write_file_atomic(
            &self.config_path(),
            config.as_bytes(),
            "internal environment configuration",
        )
        .context("could not write the internal environment configuration")?;
        security::write_file_atomic(
            &self.r_profile_path(),
            r_startup_profile().as_bytes(),
            "managed R startup profile",
        )
        .context("could not write the managed R startup profile")?;
        Ok(())
    }

    #[cfg(not(windows))]
    fn prepare_shell_prompt(&self, project_name: &str, command: &mut Command) -> Result<()> {
        let shell = env::var_os("SHELL")
            .and_then(|path| PathBuf::from(path).file_name().map(|name| name.to_owned()));
        if shell.as_deref() != Some(std::ffi::OsStr::new("zsh")) {
            return Ok(());
        }

        security::create_private_directory(&self.shell_state_dir(), "managed shell directory")?;
        security::create_private_directory(&self.zsh_state_dir(), "managed Zsh directory")?;
        security::write_file_atomic(
            &self.zshrc_path(),
            zsh_startup_profile().as_bytes(),
            "managed Zsh startup profile",
        )?;
        let user_zdotdir = env::var_os("DUAL_USER_ZDOTDIR")
            .or_else(|| env::var_os("ZDOTDIR"))
            .or_else(|| env::var_os("HOME"))
            .unwrap_or_else(|| ".".into());
        command
            .env("DUAL_PROJECT_NAME", project_name)
            .env("DUAL_USER_ZDOTDIR", user_zdotdir)
            .env("ZDOTDIR", self.zsh_state_dir());
        Ok(())
    }

    fn stage_lock(&self) -> Result<()> {
        self.ensure_state_paths_safe()?;
        self.clear_backend_lock()?;
        if self.public_lock_path().is_file() {
            let lock = read_dual_lock(&self.public_lock_path())?;
            security::write_file_atomic(
                &self.backend_lock_path(),
                lock.environment.as_bytes(),
                "staged environment lock",
            )
            .context("could not stage the environment resolution from dual.lock")?;
        }
        Ok(())
    }

    fn finish_lock(&self) -> Result<()> {
        self.clear_backend_lock()?;
        self.clear_backend_metadata()?;
        clear_engine_markers(&self.state_dir())?;
        Ok(())
    }

    fn clear_backend_lock(&self) -> Result<()> {
        if self.backend_lock_path().is_file() {
            fs::remove_file(self.backend_lock_path())
                .context("could not remove the temporary engine lock")?;
        }
        Ok(())
    }

    fn clear_backend_metadata(&self) -> Result<()> {
        let path = self.backend_metadata_path();
        security::ensure_managed_path(&self.root, &path)?;
        if path.is_dir() {
            fs::remove_dir_all(path).context("could not remove temporary engine metadata")?;
        }
        Ok(())
    }

    fn manifest_arg(&self) -> String {
        self.manifest_path().to_string_lossy().into_owned()
    }

    fn execute(&self, args: &[&str], friendly_action: &str) -> Result<()> {
        let mut command = self.internal_command(args)?;
        if self.verbose {
            eprintln!("{}", verbose_status(friendly_action));
        }
        command.stdout(Stdio::null()).stderr(Stdio::null());

        let status = run_with_timeout(&mut command, Duration::from_secs(60 * 60))
            .map_err(|error| DualError::BackendStart(error.to_string()))?;
        if !status.success() {
            anyhow::bail!("{friendly_action} failed.");
        }
        Ok(())
    }

    fn execute_task(&self, args: &[&str], task: &str) -> Result<()> {
        let status = self
            .configured_command(args)?
            .stdin(Stdio::inherit())
            .stdout(Stdio::inherit())
            .stderr(Stdio::inherit())
            .status()
            .map_err(|error| DualError::BackendStart(error.to_string()))?;
        if !status.success() {
            anyhow::bail!("Task `{task}` failed.");
        }
        Ok(())
    }

    fn capture(&self, args: &[&str]) -> Result<ExitStatus> {
        if self.verbose {
            eprintln!("{}", verbose_status("Checking project environment"));
        }
        let mut command = self.internal_command(args)?;
        command.stdout(Stdio::null()).stderr(Stdio::null());
        run_with_timeout(&mut command, Duration::from_secs(2 * 60))
            .map_err(|error| DualError::BackendStart(error.to_string()).into())
    }

    fn run_check(&self, command: &[&str]) -> bool {
        let manifest = self.manifest_arg();
        let mut args = vec![
            "run",
            "--manifest-path",
            &manifest,
            "--locked",
            "--executable",
        ];
        args.extend(command);
        if self.stage_lock().is_err() {
            return false;
        }
        let succeeded = self
            .capture(&args)
            .map(|status| status.success())
            .unwrap_or(false);
        let _ = self.finish_lock();
        succeeded
    }

    fn missing_r_packages(&self, packages: &[String]) -> Vec<String> {
        packages
            .iter()
            .filter(|package| {
                let Some(name) = r_package_name(package) else {
                    return true;
                };
                let expression = format!(
                    "quit(status=ifelse(requireNamespace('{}', quietly=TRUE), 0, 1))",
                    escape_r_string(&name)
                );
                !self.run_check(&["Rscript", "--vanilla", "-e", &expression])
            })
            .cloned()
            .collect()
    }

    fn missing_python_packages(&self, packages: &[String]) -> Vec<String> {
        packages
            .iter()
            .filter(|package| {
                let Ok(requirement) = parse_python_requirement(package) else {
                    return true;
                };
                let expression = format!(
                    "import importlib.metadata as m; m.version({:?})",
                    requirement.name
                );
                !self.run_check(&["python", "-I", "-c", &expression])
            })
            .cloned()
            .collect()
    }

    fn install_source_r_packages(
        &self,
        config: &Config,
        existing_lock: Option<&DualLock>,
        refresh: bool,
    ) -> Result<Option<RSourceLock>> {
        let packages = source_r_packages(config);
        if packages.is_empty() {
            if existing_lock.is_some_and(|lock| lock.r.is_some()) && !refresh {
                anyhow::bail!(
                    "dual.lock does not match dual.toml. Run `dual up --refresh` \
                     to remove the obsolete R source resolution."
                );
            }
            return Ok(None);
        }

        let staged_lock = self.staged_pak_lock_path();
        if staged_lock.is_file() {
            fs::remove_file(&staged_lock)?;
        }

        let locked_r = existing_lock.and_then(|lock| lock.r.as_ref());
        let use_existing_lock = locked_r.is_some() && !refresh;
        if let Some(lock) = locked_r.filter(|_| !refresh) {
            let requested = packages
                .iter()
                .map(|package| (*package).to_owned())
                .collect::<Vec<_>>();
            if lock.sources != requested {
                anyhow::bail!(
                    "dual.lock does not match dual.toml. Run `dual up --refresh` \
                     to update the R source resolution."
                );
            }
            write_json(&staged_lock, &lock.pak)
                .context("could not stage the R source resolution from dual.lock")?;
        }

        let expression = if use_existing_lock {
            format!(
                "pak::lockfile_install(lockfile={}, lib=.libPaths()[1], update=TRUE)",
                r_string_literal(&staged_lock.to_string_lossy())
            )
        } else {
            let refs = packages
                .iter()
                .map(|package| r_string_literal(package))
                .collect::<Vec<_>>()
                .join(", ");
            format!(
                concat!(
                    "pak::lockfile_create(pkg=c({refs}), lockfile={lock}, ",
                    "lib=.libPaths(), upgrade={upgrade}); ",
                    "pak::lockfile_install(lockfile={lock}, lib=.libPaths()[1], update=TRUE)"
                ),
                refs = refs,
                lock = r_string_literal(&staged_lock.to_string_lossy()),
                upgrade = if refresh { "TRUE" } else { "FALSE" },
            )
        };

        let manifest = self.manifest_arg();
        let result = self.execute(
            &[
                "run",
                "--manifest-path",
                &manifest,
                "--locked",
                "--executable",
                "Rscript",
                "--vanilla",
                "-e",
                &expression,
            ],
            "Installing source-backed R packages",
        );

        let resolved = if result.is_ok() {
            if use_existing_lock {
                locked_r.cloned()
            } else {
                let pak = read_json(&staged_lock)
                    .context("pak did not produce a readable R source resolution")?;
                Some(RSourceLock {
                    sources: packages
                        .iter()
                        .map(|package| (*package).to_owned())
                        .collect(),
                    pak,
                })
            }
        } else {
            None
        };
        if staged_lock.is_file() {
            fs::remove_file(staged_lock)?;
        }
        result.with_context(|| {
            if use_existing_lock {
                "The R source resolution from dual.lock could not be restored. Run `dual up --refresh` to re-resolve it."
            } else {
                "CRAN, Bioconductor, or GitHub R packages could not be resolved."
            }
        })?;
        Ok(resolved)
    }

    fn prepare_bridge(&self) -> Result<()> {
        self.stage_lock()?;
        let manifest = self.manifest_arg();
        let bridge = self.bridge_dir().to_string_lossy().into_owned();
        let output = self.capture(&[
            "run",
            "--manifest-path",
            &manifest,
            "--locked",
            "--executable",
            "python",
            "-I",
            "-m",
            "venv",
            "--clear",
            "--system-site-packages",
            &bridge,
        ]);
        self.finish_lock()?;
        if !output?.success() {
            anyhow::bail!("Could not prepare the R/Python bridge.");
        }
        Ok(())
    }
}

impl Backend for EnvironmentBackend {
    fn is_available(&self) -> bool {
        self.engine_path().is_some()
    }

    fn ensure_available(&self) -> Result<()> {
        if self.is_available() {
            return Ok(());
        }
        self.install_engine()
    }

    fn update_engine(&self) -> Result<()> {
        println!("Updating environment support...");
        self.prepare_engine_home()?;
        self.download_engine()?;
        println!("✓ Environment support updated to {ENGINE_VERSION}");
        Ok(())
    }

    fn uninstall_engine(&self) -> Result<bool> {
        let home = self.engine_home();
        if !home.exists() {
            return Ok(false);
        }
        security::reject_symlink(&home, "Dual environment support directory")?;
        fs::remove_dir_all(home)?;
        Ok(true)
    }

    fn migrate_lock(&self) -> Result<bool> {
        let path = self.public_lock_path();
        if !path.is_file() {
            anyhow::bail!("dual.lock was not found. Run `dual up` first.");
        }
        let contents = security::read_text_file(&path, MAX_LOCK_BYTES, "dual.lock")?;
        let lock = read_dual_lock(&path)?;
        let current = serde_json::to_string_pretty(&lock)? + "\n";
        if contents == current {
            return Ok(false);
        }
        write_dual_lock(&path, &lock)?;
        Ok(true)
    }

    fn environment_exists(&self) -> bool {
        self.ensure_state_paths_safe().is_ok()
            && self.ready_path().is_file()
            && self.manifest_path().is_file()
    }

    fn verify_manifest(&self, config: &Config) -> Result<()> {
        self.ensure_state_paths_safe()?;
        if !self.environment_exists() {
            anyhow::bail!("The project environment has not been created. Run `dual up` first.");
        }
        let expected = generate_manifest(config, &self.root)?;
        let actual =
            security::read_text_file(&self.manifest_path(), MAX_LOCK_BYTES, "generated manifest")?;
        if actual != expected {
            anyhow::bail!(
                "The generated environment manifest does not match dual.toml. \
                 Run `dual up` to regenerate it before executing project code."
            );
        }
        Ok(())
    }

    fn init_or_update(&self, config: &Config, refresh: bool) -> Result<()> {
        self.ensure_state_paths_safe()?;
        self.prepare_state()?;
        if self.ready_path().is_file() {
            fs::remove_file(self.ready_path())?;
        }
        let existing_lock = if self.public_lock_path().is_file() && !refresh {
            Some(read_dual_lock(&self.public_lock_path())?)
        } else {
            None
        };
        let generated = generate_manifest(config, &self.root)?;
        if self.manifest_path().exists() {
            let existing = security::read_text_file(
                &self.manifest_path(),
                MAX_LOCK_BYTES,
                "generated manifest",
            )
            .context("could not inspect the internal environment manifest")?;
            if !existing.starts_with(MANAGED_HEADER) {
                anyhow::bail!(
                    "The internal environment manifest was not generated by dual. \
                     Run `dual clean --yes`, then try `dual up` again."
                );
            }
        }
        let use_locked_install = should_use_locked_install(existing_lock.is_some(), refresh);

        security::write_file_atomic(
            &self.manifest_path(),
            generated.as_bytes(),
            "generated manifest",
        )
        .context("could not write generated environment configuration")?;
        if refresh {
            self.clear_backend_lock()?;
        } else {
            self.stage_lock()?;
        }
        let manifest = self.manifest_arg();
        let args = if use_locked_install {
            vec!["install", "--manifest-path", &manifest, "--locked"]
        } else {
            vec!["install", "--manifest-path", &manifest]
        };
        let result = self
            .execute(&args, "Preparing the project environment")
            .with_context(|| {
                if use_locked_install {
                    return "The shared lockfile does not match dual.toml. \
                        If dual.toml was intentionally changed, run \
                        `dual up --refresh` to update dual.lock."
                        .to_owned();
                }
                let inferred = config
                    .r
                    .packages
                    .iter()
                    .filter(|package| !known_r_mapping(package))
                    .cloned()
                    .collect::<Vec<_>>();
                if inferred.is_empty() {
                    "The requested package set could not be resolved.".to_owned()
                } else {
                    format!(
                        "Some R package names used inferred environment package names: {}.",
                        inferred.join(", ")
                    )
                }
            });
        if result.is_ok() {
            let finalize = (|| {
                let pixi = security::read_text_file(
                    &self.backend_lock_path(),
                    MAX_LOCK_BYTES,
                    "environment resolution",
                )
                .context("Dual environment support did not produce a lock resolution")?;
                let r = self.install_source_r_packages(config, existing_lock.as_ref(), refresh)?;
                write_dual_lock(
                    &self.public_lock_path(),
                    &DualLock {
                        version: 1,
                        environment: pixi,
                        r,
                    },
                )
            })();
            if let Err(error) = finalize {
                self.finish_lock()?;
                return Err(error);
            }
            self.finish_lock()?;
            if config
                .r
                .packages
                .iter()
                .filter_map(|package| r_package_name(package))
                .any(|package| package.eq_ignore_ascii_case("reticulate"))
            {
                self.prepare_bridge()?;
            }
            security::write_file_atomic(
                &self.ready_path(),
                b"ready\n",
                "environment ready marker",
            )?;
        } else {
            self.finish_lock()?;
        }
        result
    }

    fn validate(&self, config: &Config) -> Result<()> {
        if !self.run_check(&["Rscript", "--vanilla", "-e", "quit(status=0)"]) {
            anyhow::bail!("R could not start inside the project environment.");
        }
        if !self.run_check(&["python", "-I", "--version"]) {
            anyhow::bail!("Python could not start inside the project environment.");
        }

        if config
            .r
            .packages
            .iter()
            .filter_map(|package| r_package_name(package))
            .any(|package| package.eq_ignore_ascii_case("reticulate"))
        {
            let check = concat!(
                "library(reticulate); ",
                "configured <- normalizePath(Sys.getenv('RETICULATE_PYTHON'), ",
                "winslash='/', mustWork=FALSE); ",
                "actual <- normalizePath(py_config()$python, winslash='/', mustWork=FALSE); ",
                "quit(status=ifelse(nzchar(configured) && ",
                "identical(tolower(configured), tolower(actual)), 0, 1))"
            );
            if !self.run_check(&["Rscript", "--vanilla", "-e", check]) {
                anyhow::bail!(
                    "R and Python are installed, but the R/Python bridge could not use \
                     the project Python."
                );
            }
        }
        Ok(())
    }

    fn run(&self, config: &Config, task: &str) -> Result<()> {
        self.verify_manifest(config)?;
        self.ensure_state_paths_safe()?;
        self.stage_lock()?;
        let manifest = self.manifest_arg();
        let result = self.execute_task(
            &[
                "run",
                "--manifest-path",
                &manifest,
                "--locked",
                "--quiet",
                task,
            ],
            task,
        );
        self.finish_lock()?;
        result
    }

    fn shell(&self, config: &Config) -> Result<()> {
        self.verify_manifest(config)?;
        self.ensure_state_paths_safe()?;
        self.stage_lock()?;
        #[cfg(windows)]
        {
            let result = windows_shell(self, config);
            self.finish_lock()?;
            result
        }

        #[cfg(not(windows))]
        {
            unix_shell(self, config)
        }
    }

    fn clean(&self) -> Result<Vec<PathBuf>> {
        let mut removed = Vec::new();
        for path in platform::managed_paths(&self.root) {
            security::ensure_managed_path(&self.root, &path)?;
            if path.is_dir() {
                fs::remove_dir_all(&path)
                    .with_context(|| format!("could not remove {}", path.display()))?;
                removed.push(path);
            } else if path.is_file() {
                fs::remove_file(&path)
                    .with_context(|| format!("could not remove {}", path.display()))?;
                removed.push(path);
            }
        }
        Ok(removed)
    }

    fn doctor(&self, config: &Config) -> Result<BackendReport> {
        let available = self.is_available();
        let environment_present = self.environment_exists();
        let lock_present = self.public_lock_path().is_file();
        if !available || !environment_present {
            return Ok(BackendReport {
                available,
                environment_present,
                lock_present,
                ..BackendReport::default()
            });
        }

        let r_available = self.run_check(&["Rscript", "--vanilla", "-e", "quit(status=0)"]);
        let python_available = self.run_check(&["python", "-I", "--version"]);
        let missing_r_packages = if r_available {
            self.missing_r_packages(&config.r.packages)
        } else {
            Vec::new()
        };
        let missing_python_packages = if python_available {
            self.missing_python_packages(&config.python.packages)
        } else {
            Vec::new()
        };

        let bridge = if r_available && python_available {
            let reticulate_installed = self.run_check(&[
                "Rscript",
                "--vanilla",
                "-e",
                "quit(status=ifelse(requireNamespace('reticulate', quietly=TRUE), 0, 1))",
            ]);
            let uses_project_python = reticulate_installed
                && self.run_check(&[
                    "Rscript",
                    "--vanilla",
                    "-e",
                    concat!(
                        "library(reticulate); ",
                        "configured <- normalizePath(Sys.getenv('RETICULATE_PYTHON'), ",
                        "winslash='/', mustWork=FALSE); ",
                        "actual <- normalizePath(py_config()$python, winslash='/', mustWork=FALSE); ",
                        "quit(status=ifelse(nzchar(configured) && ",
                        "identical(tolower(configured), tolower(actual)), 0, 1))"
                    ),
                ]);
            Some(BridgeReport {
                reticulate_installed,
                uses_project_python,
            })
        } else {
            None
        };

        Ok(BackendReport {
            available,
            environment_present,
            lock_present,
            r_available: Some(r_available),
            python_available: Some(python_available),
            missing_r_packages,
            missing_python_packages,
            bridge,
        })
    }
}

#[cfg(not(windows))]
fn unix_shell(backend: &EnvironmentBackend, config: &Config) -> Result<()> {
    let manifest = backend.manifest_arg();
    let owned_args = [
        "shell".to_owned(),
        "--manifest-path".to_owned(),
        manifest,
        "--locked".to_owned(),
        "--change-ps1".to_owned(),
        "true".to_owned(),
    ];
    let args = owned_args.iter().map(String::as_str).collect::<Vec<_>>();
    if backend.verbose {
        eprintln!("{}", verbose_status("Opening project shell"));
    }
    let mut command = backend.configured_command(&args)?;
    backend.prepare_shell_prompt(&config.project.name, &mut command)?;
    let status = command
        .stdin(Stdio::inherit())
        .stdout(Stdio::inherit())
        .stderr(Stdio::inherit())
        .status()
        .map_err(|error| DualError::BackendStart(error.to_string()))?;
    if !status.success() {
        backend.finish_lock()?;
        return Err(DualError::BackendFailed(status.to_string()).into());
    }
    backend.finish_lock()?;
    Ok(())
}

#[cfg(windows)]
fn windows_shell(backend: &EnvironmentBackend, config: &Config) -> Result<()> {
    let manifest = backend.manifest_arg();
    let output = backend
        .internal_command(&[
            "shell-hook",
            "--manifest-path",
            &manifest,
            "--locked",
            "--json",
        ])?
        .output()
        .map_err(|error| DualError::BackendStart(error.to_string()))?;
    if !output.status.success() {
        return Err(DualError::BackendFailed(output.status.to_string()).into());
    }
    let hook: ShellHook = serde_json::from_slice(&output.stdout)
        .context("Dual environment support returned invalid shell activation data")?;
    let mut command = match windows_parent_shell() {
        WindowsShell::Cmd => {
            let mut command = Command::new(
                env::var_os("ComSpec").unwrap_or_else(|| r"C:\Windows\System32\cmd.exe".into()),
            );
            command.args(["/D", "/K"]);
            command.env("PROMPT", format!("({}) $P$G", config.project.name));
            command
        }
        WindowsShell::PowerShell => {
            let powershell = env::var_os("SystemRoot")
                .map(PathBuf::from)
                .unwrap_or_else(|| PathBuf::from(r"C:\Windows"))
                .join(r"System32\WindowsPowerShell\v1.0\powershell.exe");
            let mut command = Command::new(powershell);
            command
                .args(["-NoLogo", "-NoExit", "-Command"])
                .arg(powershell_startup_script(&config.project.name));
            command
        }
    };
    command
        .current_dir(&backend.root)
        .stdin(Stdio::inherit())
        .stdout(Stdio::inherit())
        .stderr(Stdio::inherit());
    for (name, value) in hook.environment_variables {
        command.env(name, value);
    }
    let status = command
        .status()
        .map_err(|error| DualError::BackendStart(error.to_string()))?;
    if status.success() {
        Ok(())
    } else {
        Err(DualError::BackendFailed(status.to_string()).into())
    }
}

