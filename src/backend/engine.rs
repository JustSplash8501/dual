#[cfg(not(windows))]
fn download_file(url: &str, destination: &Path) -> Result<()> {
    let destination = destination.to_string_lossy();
    let curl = Command::new("/usr/bin/curl")
        .args(["-fsSL", "-o", &destination, "--", url])
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status();
    if curl.is_ok_and(|status| status.success()) {
        return Ok(());
    }
    let status = Command::new("/usr/bin/wget")
        .args(["-qO", &destination, "--", url])
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status()
        .context("neither curl nor wget could download environment support")?;
    if status.success() {
        Ok(())
    } else {
        anyhow::bail!("environment support download failed")
    }
}

#[cfg(windows)]
fn download_file(url: &str, destination: &Path) -> Result<()> {
    const SCRIPT: &str = "$ErrorActionPreference='Stop'; Invoke-WebRequest \
        -UseBasicParsing -Uri $env:DUAL_DOWNLOAD_URL -OutFile $env:DUAL_DOWNLOAD_DESTINATION";
    let powershell = env::var_os("SystemRoot")
        .map(PathBuf::from)
        .unwrap_or_else(|| PathBuf::from(r"C:\Windows"))
        .join(r"System32\WindowsPowerShell\v1.0\powershell.exe");
    let status = Command::new(powershell)
        .args(["-NoProfile", "-ExecutionPolicy", "Bypass", "-Command"])
        .arg(SCRIPT)
        .env("DUAL_DOWNLOAD_URL", url)
        .env("DUAL_DOWNLOAD_DESTINATION", destination)
        .status()
        .context("PowerShell could not download environment support")?;
    if status.success() {
        Ok(())
    } else {
        anyhow::bail!("environment support download failed")
    }
}

fn sha256_file(path: &Path) -> Result<String> {
    let mut file = fs::File::open(path)?;
    let mut hasher = Sha256::new();
    let mut buffer = [0_u8; 64 * 1024];
    loop {
        let read = file.read(&mut buffer)?;
        if read == 0 {
            break;
        }
        hasher.update(&buffer[..read]);
    }
    Ok(format!("{:x}", hasher.finalize()))
}

fn run_with_timeout(command: &mut Command, timeout: Duration) -> Result<ExitStatus> {
    let mut child = command.spawn()?;
    match child.wait_timeout(timeout)? {
        Some(status) => Ok(status),
        None => {
            let _ = child.kill();
            let _ = child.wait();
            anyhow::bail!(
                "Dual environment support timed out after {} seconds",
                timeout.as_secs()
            )
        }
    }
}

fn unique_suffix() -> u128 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|duration| duration.as_nanos())
        .unwrap_or_default()
}

