#[cfg(not(windows))]
fn download_file(url: &str, destination: &Path) -> Result<()> {
    let destination = destination.to_string_lossy();
    let curl = Command::new("curl")
        .args(["-fsSL", url, "-o", &destination])
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status();
    if curl.is_ok_and(|status| status.success()) {
        return Ok(());
    }
    let status = Command::new("wget")
        .args(["-qO", &destination, url])
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status()
        .context("neither curl nor wget could download environment support")?;
    if status.success() {
        Ok(())
    } else {
        anyhow::bail!("environment support download failed")
    }
}

#[cfg(windows)]
fn download_file(url: &str, destination: &Path) -> Result<()> {
    let script = format!(
        "$ErrorActionPreference='Stop'; Invoke-WebRequest -UseBasicParsing -Uri {url:?} -OutFile {path:?}",
        path = destination.to_string_lossy()
    );
    let status = Command::new("powershell.exe")
        .args(["-NoProfile", "-ExecutionPolicy", "Bypass", "-Command"])
        .arg(script)
        .status()
        .context("PowerShell could not download environment support")?;
    if status.success() {
        Ok(())
    } else {
        anyhow::bail!("environment support download failed")
    }
}

fn sha256_file(path: &Path) -> Result<String> {
    let mut file = fs::File::open(path)?;
    let mut hasher = Sha256::new();
    let mut buffer = [0_u8; 64 * 1024];
    loop {
        let read = file.read(&mut buffer)?;
        if read == 0 {
            break;
        }
        hasher.update(&buffer[..read]);
    }
    Ok(format!("{:x}", hasher.finalize()))
}

fn sanitized_engine_error(bytes: &[u8]) -> String {
    String::from_utf8_lossy(bytes)
        .lines()
        .filter(|line| !line.trim().is_empty())
        .take(12)
        .map(|line| {
            line.replace("pixi", "environment engine")
                .replace("Pixi", "Environment engine")
        })
        .collect::<Vec<_>>()
        .join("\n")
}

fn sanitized_task_stderr(bytes: &[u8]) -> String {
    String::from_utf8_lossy(bytes)
        .lines()
        .filter(|line| !line.contains("Pixi task") && !line.contains("pixi task"))
        .map(|line| {
            line.replace("pixi", "environment engine")
                .replace("Pixi", "Environment engine")
        })
        .collect::<Vec<_>>()
        .join("\n")
}

fn unique_suffix() -> u128 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|duration| duration.as_nanos())
        .unwrap_or_default()
}

fn clear_engine_markers(root: &Path) -> Result<()> {
    if !root.is_dir() {
        return Ok(());
    }
    for entry in fs::read_dir(root)? {
        let entry = entry?;
        let path = entry.path();
        let file_type = entry.file_type()?;
        let name = entry.file_name();
        let name = name.to_string_lossy();
        if matches!(
            name.as_ref(),
            ".pixi" | "pixi" | "pixi_env_prefix" | ".pixi-environment-fingerprint" | "pixi.lock"
        ) {
            if file_type.is_dir() {
                fs::remove_dir_all(path)?;
            } else {
                fs::remove_file(path)?;
            }
        } else if file_type.is_dir() {
            clear_engine_markers(&path)?;
        }
    }
    Ok(())
}

