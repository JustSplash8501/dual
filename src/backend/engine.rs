fn version_constraint(version: &str) -> String {
    if version
        .chars()
        .any(|character| matches!(character, '*' | '<' | '>' | '=' | '~' | '^'))
    {
        version.to_owned()
    } else {
        format!("{version}.*")
    }
}

fn r_conda_name(package: &str) -> String {
    match package.to_ascii_lowercase().as_str() {
        "tidyverse" => "r-tidyverse".to_owned(),
        "lme4" => "r-lme4".to_owned(),
        "emmeans" => "r-emmeans".to_owned(),
        "broom" => "r-broom".to_owned(),
        "reticulate" => "r-reticulate".to_owned(),
        other => format!("r-{other}"),
    }
}

fn is_source_r_package(package: &str) -> bool {
    let reference = package
        .split_once('=')
        .map(|(_, reference)| reference)
        .unwrap_or(package);
    matches!(
        reference.split_once("::").map(|(source, _)| source),
        Some("cran" | "bioc" | "github")
    )
}

fn conda_r_packages(config: &Config) -> impl Iterator<Item = &str> {
    config
        .r
        .packages
        .iter()
        .filter(|package| !is_source_r_package(package))
        .map(String::as_str)
}

fn source_r_packages(config: &Config) -> Vec<&str> {
    let mut packages = config
        .r
        .packages
        .iter()
        .filter(|package| is_source_r_package(package))
        .map(String::as_str)
        .collect::<Vec<_>>();
    packages.sort_unstable();
    packages
}

fn r_package_name(package: &str) -> Option<String> {
    if let Some((name, reference)) = package.split_once('=') {
        if is_source_r_package(reference) {
            return Some(name.to_owned());
        }
    }

    let (source, reference) = match package.split_once("::") {
        Some(parts) => parts,
        None => return Some(package.to_owned()),
    };
    match source {
        "cran" | "bioc" => reference
            .split(['@', '?'])
            .next()
            .filter(|name| !name.is_empty())
            .map(str::to_owned),
        "github" => reference
            .split(['@', '#', '?'])
            .next()
            .and_then(|repository| repository.split('/').nth(1))
            .map(|repository| repository.trim_end_matches(".git"))
            .filter(|name| !name.is_empty())
            .map(str::to_owned),
        _ => None,
    }
}

