pub fn generate_manifest(config: &Config, root: &Path) -> Result<String> {
    let project_root = root.to_string_lossy().into_owned();
    let mut dependencies = BTreeMap::new();
    dependencies.insert("r-base".to_owned(), version_constraint(&config.r.version));
    dependencies.insert(
        "python".to_owned(),
        version_constraint(&config.python.version),
    );
    for package in conda_r_packages(config) {
        dependencies.insert(r_conda_name(package), "*".to_owned());
    }
    if !source_r_packages(config).is_empty() {
        dependencies.insert("r-pak".to_owned(), "*".to_owned());
    }

    let pypi_dependencies = config
        .python
        .packages
        .iter()
        .map(|package| {
            let requirement = parse_python_requirement(package)?;
            let dependency = if requirement.extras.is_empty() {
                PypiDependency::Version(requirement.version)
            } else {
                PypiDependency::Detailed {
                    version: requirement.version,
                    extras: requirement.extras,
                }
            };
            Ok((requirement.name, dependency))
        })
        .collect::<Result<BTreeMap<_, _>>>()?;

    let manifest = PyProject {
        project: PyProjectMetadata {
            name: config.project.name.clone(),
            version: "0.0.0".to_owned(),
        },
        tool: ToolTable {
            pixi: PixiManifest {
                workspace: Workspace {
                    name: config.project.name.clone(),
                    channels: vec!["conda-forge".to_owned()],
                    platforms: vec![
                        "linux-64".to_owned(),
                        "osx-64".to_owned(),
                        "osx-arm64".to_owned(),
                        "win-64".to_owned(),
                    ],
                },
                dependencies,
                pypi_dependencies,
                tasks: config
                    .tasks
                    .iter()
                    .map(|(name, command)| {
                        (
                            name.clone(),
                            TaskSpec {
                                cmd: command.clone(),
                                cwd: "../..".to_owned(),
                            },
                        )
                    })
                    .collect(),
                target: BTreeMap::from([
                    (
                        "unix".to_owned(),
                        TargetConfig {
                            activation: Activation {
                                env: BTreeMap::from([
                                    ("DUAL_PROJECT_ROOT".to_owned(), project_root.clone()),
                                    (
                                        "R_PROFILE_USER".to_owned(),
                                        "$PIXI_PROJECT_ROOT/../Rprofile".to_owned(),
                                    ),
                                    (
                                        "RETICULATE_PYTHON".to_owned(),
                                        "$PIXI_PROJECT_ROOT/../bridge/bin/python".to_owned(),
                                    ),
                                    ("RETICULATE_USE_MANAGED_VENV".to_owned(), "no".to_owned()),
                                ]),
                            },
                        },
                    ),
                    (
                        "win".to_owned(),
                        TargetConfig {
                            activation: Activation {
                                env: BTreeMap::from([
                                    ("DUAL_PROJECT_ROOT".to_owned(), project_root),
                                    (
                                        "R_PROFILE_USER".to_owned(),
                                        "%PIXI_PROJECT_ROOT%\\..\\Rprofile".to_owned(),
                                    ),
                                    (
                                        "RETICULATE_PYTHON".to_owned(),
                                        "%PIXI_PROJECT_ROOT%\\..\\bridge\\Scripts\\python.exe"
                                            .to_owned(),
                                    ),
                                    ("RETICULATE_USE_MANAGED_VENV".to_owned(), "no".to_owned()),
                                ]),
                            },
                        },
                    ),
                ]),
            },
        },
    };

    Ok(format!(
        "{MANAGED_HEADER}{}",
        toml::to_string_pretty(&manifest)?
    ))
}

