    #[test]
    fn reads_legacy_lock_field_but_writes_neutral_field() {
        let directory = tempfile::tempdir().unwrap();
        let path = directory.path().join("dual.lock");
        fs::write(&path, r#"{"version":1,"pixi":"legacy"}"#).unwrap();
        let lock = read_dual_lock(&path).unwrap();
        assert_eq!(lock.environment, "legacy");
        write_dual_lock(&path, &lock).unwrap();
        let contents = fs::read_to_string(path).unwrap();
        assert!(contents.contains("\"environment\""));
        assert!(!contents.contains("\"pixi\""));
    }

    #[test]
    fn removes_private_engine_marker_names() {
        let directory = tempfile::tempdir().unwrap();
        let metadata = directory.path().join("env/conda-meta");
        fs::create_dir_all(metadata.join(".pixi")).unwrap();
        for name in [
            "pixi",
            "pixi_env_prefix",
            ".pixi-environment-fingerprint",
            "pixi.lock",
        ] {
            fs::write(metadata.join(name), "marker").unwrap();
        }
        fs::write(metadata.join("history"), "keep").unwrap();

        clear_engine_markers(directory.path()).unwrap();

        assert!(metadata.join("history").is_file());
        assert!(!metadata.join(".pixi").exists());
        assert!(!metadata.join("pixi").exists());
        assert!(!metadata.join("pixi_env_prefix").exists());
        assert!(!metadata.join(".pixi-environment-fingerprint").exists());
        assert!(!metadata.join("pixi.lock").exists());
    }
