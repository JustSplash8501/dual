fn read_dual_lock(path: &Path) -> Result<DualLock> {
    let lock: DualLock = serde_json::from_slice(
        &security::read_file(path, MAX_LOCK_BYTES, "dual.lock")
            .with_context(|| format!("could not read {}", path.display()))?,
    )
    .with_context(|| {
        format!(
            "{} is not a supported dual lockfile; run `dual up --refresh`",
            path.display()
        )
    })?;
    if lock.version != 1 {
        anyhow::bail!("{} uses an unsupported lockfile version", path.display());
    }
    Ok(lock)
}

fn read_json(path: &Path) -> Result<serde_json::Value> {
    serde_json::from_slice(
        &security::read_file(path, MAX_LOCK_BYTES, "generated package lock")
            .with_context(|| format!("could not read {}", path.display()))?,
    )
    .with_context(|| format!("could not parse {}", path.display()))
}

fn write_json(path: &Path, value: &impl Serialize) -> Result<()> {
    let mut contents = serde_json::to_vec_pretty(value)?;
    contents.push(b'\n');
    if contents.len() as u64 > MAX_LOCK_BYTES {
        anyhow::bail!("generated package lock exceeds the safety limit");
    }
    security::write_file_atomic(path, &contents, "generated package lock")
        .with_context(|| format!("could not write {}", path.display()))
}

fn write_dual_lock(path: &Path, lock: &DualLock) -> Result<()> {
    let mut contents = serde_json::to_vec_pretty(lock)?;
    contents.push(b'\n');
    if contents.len() as u64 > MAX_LOCK_BYTES {
        anyhow::bail!("dual.lock exceeds the safety limit");
    }
    security::write_file_atomic(path, &contents, "dual.lock")
}

